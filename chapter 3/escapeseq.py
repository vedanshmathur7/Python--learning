print("vedansh mathur is a\ngood boy") #  \n prints a new line
print("vedansh mathur is a\tgood boy") #  \t gives a bar space
print("vedansh mathur is a \'good\' boy") #  \' prints a single quote
print("vedansh mathur is a \"good\" boy") #  \" prints a double quote
print("vedansh mathur is a \\good\\ boy") #  \\ prints a backslash
